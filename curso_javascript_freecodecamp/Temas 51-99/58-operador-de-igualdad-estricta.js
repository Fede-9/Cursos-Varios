// IMPORTANTE: El operador de estricta igualdad ( === ) revisa si dos operandos son iguales y produce un resultado Booleano. A diferencia del operador de igualdad regular (==), el operador de estricta igualdad siempre considera que los operandos de distinto tipo de valor son diferentes y nunca similares.


console.log(9 == 9);   // true
console.log(9 == "9"); // true

console.log(9 === 9);   // true
console.log(9 === "9"); // false
